public class Ypallhlos {
	private String onoma;
	private int kwdikos;
	private int energies;

	public Ypallhlos(String onoma, int kwdikos, int energies) {
		this.onoma = onoma;
		this.kwdikos = kwdikos;
		this.energies = energies;
	}

	public String getOnoma() {
		return onoma;
	}

	public void setOnoma(String onoma) {
		this.onoma = onoma;
	}

	public int getKwdikos() {
		return kwdikos;
	}

	public void setKwdikos(int kwdikos) {
		this.kwdikos = kwdikos;
	}

	public int getEnergies() {
		return energies;
	}

	public void setEnergies(int energies) {
		this.energies = energies;
	}
}
